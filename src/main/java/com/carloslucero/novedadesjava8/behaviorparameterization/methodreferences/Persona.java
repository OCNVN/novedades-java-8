public class Persona {
    public String nombre;
    public String apellido;
    public Integer edad;
    public String ciudad;
    public String pais;
    public String dni;

    public Persona(String nombre, String apellido, Integer edad, String ciudad, String pais, String dni) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;
        this.ciudad = ciudad;
        this.pais = pais;
        this.dni = dni;
    }
}
